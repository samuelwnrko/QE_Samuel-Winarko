package org.example;

public class Calculator {
    //method penjumlahan
    public Integer add(Integer x, Integer y){
        Integer hasil = x+y;
        return hasil;
    }
    //method pengurangan
    public Integer substract(Integer x, Integer y){
        Integer hasil = x-y;
        return hasil;
    }
    //method perkalian
    public Integer multiply(Integer x, Integer y){
        Integer hasil = x*y;
        return hasil;
    }
    //method pembagian
    public Integer divide(Integer x, Integer y){
        Integer hasil = x/y;
        return hasil;
    }
}

